@trains = []

def action(train, temp_trains)
  cur_position = train[:position]
  cur_direction = train[:direction]
  cur_turn = train[:turn]

  new_train = train.clone

  next_tile = case cur_direction
              when 'up'
                @input[cur_position[0] - 1][cur_position[1]]
              when 'down'
                @input[cur_position[0] + 1][cur_position[1]]
              when 'left'
                @input[cur_position[0]][cur_position[1] - 1]
              else
                @input[cur_position[0]][cur_position[1] + 1]
              end

  next_move = move(cur_position, cur_direction)
  if @trains.select { |t| t[:position] == next_move && t[:train_no] != train[:train_no] }.any? || temp_trains.select { |t| t[:position] == next_move && t[:train_no] != train[:train_no] }.any?
    return [nil, true, [next_move, cur_position]]
  end

  case next_tile
  when '\\', '/'
    new_train[:direction] = char_to_direction(turn(next_tile, cur_direction, cur_turn))
  when '+'
    new_train[:direction] = char_to_direction(turn(next_tile, cur_direction, cur_turn))
    new_train[:turn] = next_turn(cur_turn)
  end
  new_train[:position] = next_move

  # next_move = move(new_train[:position], new_train[:direction])
  # puts "#{new_train[:direction]} #{next_move}"
  # [nil, true, next_move] unless @trains.select { |t| t[:position] == next_move }.empty?
  # [new_train, false, []]
  [new_train, false, []]
end

def turn(track_type, current_direction, current_turn)
  case track_type
  when '+'
    case current_direction
    when 'down'
      case current_turn
      when 'left'
        '>'
      when 'right'
        '<'
      else
        'v'
      end
    when 'up'
      case current_turn
      when 'left'
        '<'
      when 'right'
        '>'
      else
        '^'
      end
    when 'left'
      case current_turn
      when 'left'
        'v'
      when 'right'
        '^'
      else
        '<'
      end
    else
      case current_turn
      when 'left'
        '^'
      when 'right'
        'v'
      else
        '>'
      end
    end
  when '\\'
    case current_direction
    when 'left'
      '^'
    when 'right'
      'v'
    when 'up'
      '<'
    else
      '>'
    end
  when '/'
    case current_direction
    when 'left'
      'v'
    when 'right'
      '^'
    when 'up'
      '>'
    else
      '<'
    end
  end
end

def move(current_postion, current_direction)
  i = current_postion[0]
  j = current_postion[1]

  case current_direction
  when 'up'
    [i - 1, j]
  when 'down'
    [i + 1, j]
  when 'left'
    [i, j - 1]
  else
    [i, j + 1]
  end
end

def next_turn(current_turn)
  case current_turn
  when 'left'
    'straight'
  when 'straight'
    'right'
  else
    'left'
  end
end

def char_to_direction(c)
  case c
  when '>'
    'right'
  when '<'
    'left'
  when '^'
    'up'
  else
    'down'
  end
end

def direction_to_char(dir)
  case dir
  when 'left'
    '<'
  when 'right'
    '>'
  when 'up'
    '^'
  else
    'v'
  end
end

def put_trains_on_tracks(trains)
  tracks = @input.clone

  trains.each do |t|
    tracks[t[:position][0]][t[:position][1]] = direction_to_char(t[:direction])
  end
  tracks
end

